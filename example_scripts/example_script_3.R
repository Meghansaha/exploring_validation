#example_script_3 - Table Scan Workflow----------------------------------------#

#==============================================================================#
#Library Load in----------------------------------------------------------------
#==============================================================================#
library(readr) #For reading in CSVs
library(pointblank) #For validation help

#==============================================================================#
#Data Load-in-------------------------------------------------------------------
#==============================================================================#
example_data <- read_csv("./data/example_data.csv", show_col_types = FALSE)

#==============================================================================#
#Scan the Data------------------------------------------------------------------
#==============================================================================#
#Use the scan_data function to generate an HTML output#
example_data %>%
  scan_data()

#==============================================================================#
#Set Options if Desired---------------------------------------------------------
#==============================================================================#
#Use the scan_data function to generate an HTML output with options#
example_data %>%
  scan_data(sections = "OV",
            navbar = FALSE,
            lang = "es",
            width = 500,
            locale = "es_ES")